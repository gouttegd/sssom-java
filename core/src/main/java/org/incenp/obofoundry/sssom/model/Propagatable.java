package org.incenp.obofoundry.sssom.model;

import static java.lang.annotation.ElementType.FIELD;
import static java.lang.annotation.RetentionPolicy.RUNTIME;

import java.lang.annotation.Retention;
import java.lang.annotation.Target;

/**
 * An annotation to indicate that a SSSOM slot is "propagatable".
 * 
 * A propagatable slot is a slot that, when used on a {@link MappingSet} object,
 * represents a common value for all mappings in the set.
 */
@Retention(RUNTIME)
@Target(FIELD)
public @interface Propagatable {

}
