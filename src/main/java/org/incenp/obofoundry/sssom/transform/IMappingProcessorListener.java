package org.incenp.obofoundry.sssom.transform;

import org.incenp.obofoundry.sssom.model.Mapping;

/**
 * A listener for the generation of an object from the application of a rule to
 * a mapping.
 *
 * @param <T> The type of object generated by the mapping processing rule.
 */
public interface IMappingProcessorListener<T> {

    /**
     * This method is called whenever a rule is applied to a mapping and produces an
     * object.
     * 
     * @param rule    The rule that is being applied.
     * @param mapping The mapping the rule is being applied to.
     * @param product The object produced by the rule when applied to this
     *                particular mapping.
     */
    public void generated(MappingProcessingRule<T> rule, Mapping mapping, T product);

}
